#!/usr/bin/env node
/**
 * Scan public/cases/* and emit public/cases/index.json so the portal
 * can fuzzy-match aligner setup reviews to portal_orders without an
 * extra D1 round-trip on every page load.
 *
 * The case HTML embeds a JS literal:
 *
 *   const CASE_DATA = {"case_id": "...", "patient": {...},
 *                      "doctor": {...}, "generated_date": "..."};
 *
 * We extract that object via a single regex (anchored to the literal
 * `const CASE_DATA = ` prefix so a stray `CASE_DATA` mention elsewhere
 * in the file can't fool us). JSON.parse is safe because the embedded
 * literal is generated by the case-builder pipeline and is strict JSON.
 *
 * Output shape (sorted by generated_date DESC then slug):
 *
 *   [
 *     {
 *       "slug": "kawamoto-mika-flrv0b",
 *       "patient_first": "Mika",
 *       "patient_full": "Mika Young",
 *       "doctor_last": "Kawamoto",
 *       "doctor_name": "Dr. Kawamoto",
 *       "generated_date": "2026-05-14",
 *       "status": "awaiting_approval"
 *     },
 *     ...
 *   ]
 *
 * Hook this into `npm run build` so the manifest stays in sync without
 * any operator step. Skips silently when public/cases/ is missing
 * (fresh checkouts, CI without the data dir).
 */

import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const CASES_DIR = path.join(ROOT, "public", "cases");
const OUT_FILE = path.join(CASES_DIR, "index.json");

// `const CASE_DATA = { ... };` — match the {...} as group 1. Non-greedy
// `[\s\S]+?` so embedded `}` inside CASE_DATA doesn't truncate. End
// anchor on `};` so we don't run away into the rest of the file.
const CASE_DATA_RE = /const\s+CASE_DATA\s*=\s*(\{[\s\S]+?\})\s*;/;

function deriveDoctorLast(slug, doctorName) {
  // Slug format: {doctor_last}-{patient_first}-{6char}. Take the first
  // dash-segment, lowercase it.
  const m = /^([a-z0-9]+)-/i.exec(slug);
  if (m) return m[1].toLowerCase();
  // Fallback: trim "Dr. " prefix + take last token.
  const stripped = (doctorName || "").replace(/^Dr\.?\s*/i, "").trim();
  const parts = stripped.split(/\s+/);
  return (parts[parts.length - 1] || "").toLowerCase();
}

async function readCaseEntry(slug) {
  const html = await fs.readFile(
    path.join(CASES_DIR, slug, "index.html"),
    "utf-8",
  );
  const m = CASE_DATA_RE.exec(html);
  if (!m) {
    console.warn(`[cases-manifest] ${slug}: CASE_DATA literal not found, skipping`);
    return null;
  }
  let data;
  try {
    data = JSON.parse(m[1]);
  } catch (err) {
    console.warn(
      `[cases-manifest] ${slug}: CASE_DATA failed to parse — ${err.message}`,
    );
    return null;
  }
  const patient = data.patient || {};
  const doctor = data.doctor || {};
  const patientFirst = (patient.first_name || "").trim();
  const patientFull = (patient.name || "").trim();
  const doctorName = (doctor.name || "").trim();
  const doctorLast = deriveDoctorLast(slug, doctorName);

  return {
    slug,
    patient_first: patientFirst,
    patient_full: patientFull,
    doctor_last: doctorLast,
    doctor_name: doctorName,
    generated_date: (data.generated_date || "").trim() || null,
    status: (data.status || "").trim() || null,
  };
}

async function main() {
  let dirents;
  try {
    dirents = await fs.readdir(CASES_DIR, { withFileTypes: true });
  } catch (err) {
    if (err.code === "ENOENT") {
      console.log(
        "[cases-manifest] public/cases/ not present; nothing to scan.",
      );
      return;
    }
    throw err;
  }

  const slugs = dirents
    .filter((d) => d.isDirectory() && d.name !== "index.json")
    .map((d) => d.name);

  const entries = [];
  for (const slug of slugs) {
    const entry = await readCaseEntry(slug);
    if (entry) entries.push(entry);
  }

  // Newest first, with slug as a stable tiebreaker so two cases on the
  // same generated_date keep a predictable order across builds.
  entries.sort((a, b) => {
    const dateCmp = String(b.generated_date || "").localeCompare(
      String(a.generated_date || ""),
    );
    if (dateCmp !== 0) return dateCmp;
    return a.slug.localeCompare(b.slug);
  });

  await fs.writeFile(OUT_FILE, JSON.stringify(entries, null, 2) + "\n");
  console.log(
    `[cases-manifest] wrote ${entries.length} entries → ${path.relative(ROOT, OUT_FILE)}`,
  );
}

main().catch((err) => {
  console.error("[cases-manifest] fatal:", err);
  process.exit(1);
});
