/**
 * Phase 1.4d-5 — Bulk clinic import from VisualDLP master.
 *
 * Reads C:\ASOAutomation\visualdlp_clinics.json (453 entries produced by
 * dump_visualdlp_doctors.py) and emits SQL INSERT OR IGNORE statements
 * for each clinic into portal-clinics-import.sql, ready to apply via:
 *
 *   npx wrangler d1 execute aso-chat-logs --remote --file=portal-clinics-import.sql
 *
 * Idempotency:
 *   - aso_account_number is UNIQUE in the schema → re-imports skip rows
 *     whose ArAccountNumber is already present (covers Jason Ikeno from
 *     the Phase 1.4b-2 manual seed).
 *   - visualdlp_account_id is UNIQUE via partial index (migration 0005)
 *     so re-imports of the same AccountId also skip cleanly.
 *
 * Placeholder contact_email: portal_clinics.contact_email is NOT NULL
 * but the VisualDLP master doesn't ship an email per clinic. We use
 * `pending@asohawaii.invalid` (RFC 2606 reserved TLD — guaranteed not
 * to deliver anywhere) so aso_staff can spot and edit these via the
 * Clinics CRUD UI as needed. The "invalid" TLD also makes them trivial
 * to filter when picking clinics that haven't had their real contact
 * captured yet (`WHERE contact_email LIKE '%@asohawaii.invalid'`).
 *
 * Skipped:
 *   - "Test Account" (1 row, VisualDLP dev clinic, no ArAccountNumber)
 *   - any clinic with empty Name (none observed)
 *
 * Output is written via --out to bypass PowerShell's UTF-16 LE redirect
 * gotcha (feedback_powershell_redirect_utf16.md).
 */
import { readFileSync, writeFileSync } from "node:fs";
import { argv } from "node:process";

const args = new Map();
for (let i = 2; i < argv.length; i++) {
  if (argv[i].startsWith("--")) {
    const [k, v] = argv[i].slice(2).split("=", 2);
    args.set(k, v ?? argv[++i]);
  }
}

const SRC =
  args.get("src") ?? "C:/ASOAutomation/visualdlp_clinics.json";
const OUT = args.get("out") ?? "portal-clinics-import.sql";
const PLACEHOLDER_EMAIL = "pending@asohawaii.invalid";

const data = JSON.parse(readFileSync(SRC, "utf8"));
const clinics = data.clinics ?? [];

/** SQL string literal — replace ' with '' (no \, that's MySQL not SQLite). */
function sql(s) {
  if (s == null) return "NULL";
  return "'" + String(s).replace(/'/g, "''") + "'";
}

let written = 0;
let skipped = 0;
const skippedReasons = new Map();
// D1 wraps each statement in its own transaction and rejects explicit
// BEGIN TRANSACTION / SAVEPOINT — verified via wrangler error: "To
// execute a transaction, please use state.storage.transaction() ...".
// We just emit bare INSERT OR IGNORE statements; if any fail the rest
// continue and the totals are reported by D1.
const lines = [
  "-- Bulk clinic import — generated by scripts/import-clinics-from-vdlp.mjs",
  `-- Source: ${SRC}`,
  `-- Generated: ${new Date().toISOString()}`,
  "",
];

for (const c of clinics) {
  const name = (c.Name ?? "").trim();
  const vdlpId = (c.AccountId ?? "").trim();
  const arNum = (c.ArAccountNumber ?? "").trim();
  const businessGroup = (c.BusinessGroupName ?? "").trim() || null;

  // Skip obvious test rows and corrupted entries.
  if (!name || name.toLowerCase() === "test account") {
    skipped++;
    skippedReasons.set(
      "test_or_empty_name",
      (skippedReasons.get("test_or_empty_name") ?? 0) + 1,
    );
    continue;
  }
  if (!vdlpId) {
    skipped++;
    skippedReasons.set(
      "no_visualdlp_account_id",
      (skippedReasons.get("no_visualdlp_account_id") ?? 0) + 1,
    );
    continue;
  }

  // BusinessGroupName might be useful as a hint for grouping branches
  // (e.g. "Hawaii Family Dental @Kapolei" → group "Hawaii Family
  // Dental"). We don't map it to a portal_clinics column today but
  // surface it in email_domain as a placeholder for future grouping
  // logic — admin can override via Clinics CRUD.
  void businessGroup; // reserved for a follow-up cleanup

  lines.push(
    `INSERT OR IGNORE INTO portal_clinics ` +
      `(name, visualdlp_account_id, aso_account_number, contact_email) ` +
      `VALUES (${sql(name)}, ${sql(vdlpId)}, ${sql(arNum || null)}, ${sql(PLACEHOLDER_EMAIL)});`,
  );
  written++;
}

lines.push("");

writeFileSync(OUT, lines.join("\n"), { encoding: "utf8" });

const skipReport = [...skippedReasons.entries()]
  .map(([k, v]) => `${k}=${v}`)
  .join(", ");

console.log(
  `wrote ${written} INSERT(s) to ${OUT}; skipped ${skipped} (${skipReport || "—"})`,
);
